# String.py

# 큰 따옴표
print("Hello world")

# 작은 따옴표
print('Python is fun')

# 큰 따옴표 3개
print("""Life is short, You need python""")

# 작은 따옴표 3개
print('Life is short, You need python')

# 문자 안에 작은 따옴표를 포함
food = "Python's favorite food is perl"
print(food)

# 문자 안에 큰 따옴표를 포함
food = '"Python is very easy." he says.'
print(food)

# 백슬래시를 이용해서 작은 따옴표와 큰따옴표를 문자열에 포함
food = 'Python\'s favorite food is perl'
print(food)

food = "\"Python is very easy.\" he says."
print(food)

# 여러줄인 문자열
multiline = "Life is too short\nYou need Python"
print(multiline)

multiline = '''
Life is too short
You need Python
'''
print(multiline)

multiline = """
Life is too short
You need Python
"""
print(multiline)